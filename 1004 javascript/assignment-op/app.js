/**
 * assignment operators
 * =,+=,_=,/=,*=,%=
 * 
 */
let a = 10;

// a += 2   a = a + 2
a += 2;
console.log('+= assignment operators >  '  + a); 

// a -= 2  a = a-2 
a -= 2;
console.log('-= assignment operators >  '  + a); 

//a %=2  a = a % 2
a %= 2
console.log('%= assignment operators >  '  + a); 